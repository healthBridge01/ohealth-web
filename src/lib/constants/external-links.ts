/** Public URLs for app stores, social profiles, and marketing CTAs. */

export function getIosAppStoreUrl(): string | null {
  const url = process.env.NEXT_PUBLIC_IOS_APP_URL?.trim();
  return url || null;
}

export function getGooglePlayUrl(): string | null {
  const url = process.env.NEXT_PUBLIC_ANDROID_APP_URL?.trim();
  return url || null;
}

